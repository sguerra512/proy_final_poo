    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblCandidatos.getSelectedRow();
        
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un candidato de la tabla");
            return;
        }

        Object objDni = tblCandidatos.getValueAt(fila, 2);
        if (objDni == null) return; 

        String dni = objDni.toString();

        int respuesta = JOptionPane.showConfirmDialog(this, 
            "¿Seguro que deseas sacar de la contienda al candidato con DNI " + dni + "?", 
            "Confirmación", JOptionPane.YES_NO_OPTION);
        
        if (respuesta == JOptionPane.YES_OPTION) {
            boolean exito = motor.getGestorCandidatos().eliminarCandidato(dni);
            
            if (exito) {
                JOptionPane.showMessageDialog(this, "Candidato eliminado con éxito.");
                actualizarTablaCandidatos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error: No se encontró el DNI en el registro.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblCandidatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCandidatosMouseClicked
        int fila = tblCandidatos.getSelectedRow();
        if (fila != -1) {
            Object objNombre = tblCandidatos.getValueAt(fila, 0);
            Object objApellido = tblCandidatos.getValueAt(fila, 1);
            Object objDni = tblCandidatos.getValueAt(fila, 2);
            Object objPartido = tblCandidatos.getValueAt(fila, 3);

            txtNombre.setText(objNombre != null ? objNombre.toString() : "");
            txtApellido.setText(objApellido != null ? objApellido.toString() : "");
            txtDni.setText(objDni != null ? objDni.toString() : "");
            

            if (objPartido != null) {
                cmbPartidos.setSelectedItem(objPartido.toString());
            }
        }
               
                
    }//GEN-LAST:event_tblCandidatosMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String dni = txtDni.getText().trim();
        String nuevosNombres = txtNombre.getText().trim();
        String nuevosApellidos = txtApellido.getText().trim();

        if (dni.isEmpty() || nuevosNombres.isEmpty() || nuevosApellidos.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete todos los campos obligatorios.");
            return;
        }

        Candidato candidato = motor.getGestorCandidatos().buscarPorDni(dni);

        if (candidato != null) {
            candidato.setNombres(nuevosNombres);
            candidato.setApellidos(nuevosApellidos);
            
            // Si también modificas el partido desde un ComboBox:
            // PartidoPolitico nuevoPartido = motor.getGestorPartidos().buscarPorSigla(cmbPartidos.getSelectedItem().toString());
            // candidato.setPartido(nuevoPartido);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Candidato modificado con éxito.");
            
            txtDni.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            
            actualizarTablaCandidatos(); 
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se puede modificar el DNI");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void llenarComboPartidos() {
        // 1. Limpiamos el combo por si acaso
        cmbPartidos.removeAllItems();
        
        // 2. Traemos la lista de partidos desde el backend
        PartidoPolitico[] partidos = motor.getGestorPartidos().getBdPartidos();
        int total = motor.getGestorPartidos().getCantidadActual();
        
        // 3. Llenamos el combo solo con los nombres
        for (int i = 0; i < total; i++) {
            cmbPartidos.addItem(partidos[i].getNombre());
        }
    }
    
    private void actualizarTablaCandidatos() {
    DefaultTableModel modelo = (DefaultTableModel) tblCandidatos.getModel();
    modelo.setRowCount(0);

    Candidato[] lista = motor.getGestorCandidatos().getBdCandidatos();
    int total = motor.getGestorCandidatos().getCantidadActual();

    for (int i = 0; i < total; i++) {
        Object[] fila = {
            lista[i].getNombres(),
            lista[i].getApellidos(), // <--- No olvides el getter del apellido
            lista[i].getDni(),
            lista[i].getPartido().getNombre()
        };
        modelo.addRow(fila);
    }
}
    
    private void limpiarCampos() {
    txtNombre.setText("");
    txtApellido.setText("");
    txtDni.setText("");
    txtNombre.requestFocus(); // Pone el cursor listo para el siguiente
}
    

