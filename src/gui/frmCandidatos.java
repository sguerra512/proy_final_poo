    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    String nombre = txtNombre.getText().trim();
    String apellido = txtApellido.getText().trim();
    String dni = txtDni.getText().trim();
    int indexPartido = cmbPartidos.getSelectedIndex();

    if (nombre.isEmpty() || apellido.isEmpty() || dni.isEmpty() || indexPartido == -1) {
        JOptionPane.showMessageDialog(this, "Error: Todos los campos son obligatorios.");
        return;
    }

    PartidoPolitico partidoSeleccionado = motor.getGestorPartidos().getBdPartidos()[indexPartido];

    
    boolean exito = motor.getGestorCandidatos().registrarCandidato(nombre, apellido, dni,partidoSeleccionado);

    if (exito) {
        JOptionPane.showMessageDialog(this, "Candidato " + apellido + " registrado correctamente.");
        actualizarTablaCandidatos();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo registrar (Arreglo lleno o DNI duplicado).");
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

