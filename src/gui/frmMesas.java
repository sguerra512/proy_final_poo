    private void btnAdministrarMesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdministrarMesaActionPerformed
        int fila = tblMesas.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona una mesa para administrarla.");
        return;
    }

    String idMesaSeleccionada = tblMesas.getValueAt(fila, 0).toString();

    frmDetalleMesa ventanaDetalle = new frmDetalleMesa(this.motor, idMesaSeleccionada,this);
    ventanaDetalle.setVisible(true);
    }//GEN-LAST:event_btnAdministrarMesaActionPerformed

