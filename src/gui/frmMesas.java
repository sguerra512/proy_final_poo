    private void btnGuardarMesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarMesaActionPerformed
        String idMesa = txtIdMesa.getText().trim();
    String capTexto = txtCapacidad.getText().trim();

    if (idMesa.isEmpty() || capTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Campos vacíos.");
        return;
    }

    try {
        int capacidad = Integer.parseInt(capTexto);
        
        if (capacidad <= 0) {
            JOptionPane.showMessageDialog(this, "La capacidad debe ser mayor a cero.");
            return;
        }

        boolean exito = motor.getGestorMesas().registrarMesa(idMesa, capacidad);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Mesa " + idMesa + " registrada.");
            actualizarTablaMesas();
            txtIdMesa.setText("");
            txtCapacidad.setText("");
            txtIdMesa.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Error: Arreglo lleno o Mesa ya existe.");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "La capacidad debe ser un número entero válido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnGuardarMesaActionPerformed

