    private void btnEliminarMesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMesaActionPerformed
        
        int fila = tblMesas.getSelectedRow();
        
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una mesa de la tabla primero. No dispares a ciegas.");
            return;
        }

        Object objIdMesa = tblMesas.getValueAt(fila, 0);
        if (objIdMesa == null) return;

        String idMesa = objIdMesa.toString();

        int respuesta = JOptionPane.showConfirmDialog(this, 
            "¿Seguro que deseas eliminar la Mesa " + idMesa + "? \nADVERTENCIA: Esto eliminará también a todos los miembros asignados a ella.", 
            "Confirmación de impacto", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            
        if (respuesta == JOptionPane.YES_OPTION) {
            boolean exito = motor.getGestorMesas().eliminarMesa(idMesa);
            
            if (exito) {
                JOptionPane.showMessageDialog(this, "Mesa y miembros eliminados.");
                actualizarTablaMesas(); 
            } else {
                JOptionPane.showMessageDialog(this, "Error de inconsistencia: La mesa no se encontró en el registro.");
            }
        }
    }//GEN-LAST:event_btnEliminarMesaActionPerformed

    /**
     * @param args the command line arguments
     */
    protected void actualizarTablaMesas() {
    DefaultTableModel modelo = (DefaultTableModel) tblMesas.getModel();
    modelo.setRowCount(0); 

   
    modelo.MesaElectoral[] lista = motor.getGestorMesas().getBdMesas();
    int total = motor.getGestorMesas().getCantidadActual();

    for (int i = 0; i < total; i++) {
        
        Object[] fila = {
            lista[i].getIdMesa() 
        };
        modelo.addRow(fila);
    }
}
    

    
    
    

