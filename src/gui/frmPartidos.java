    private void btnGuardarActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionActionPerformed
    String nombre = txtNombre.getText().trim();
    String siglas = txtSiglas.getText().trim();
    String lider = txtLider.getText().trim();

    if (nombre.isEmpty() || siglas.isEmpty() || lider.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No dejes campos vacíos, ingeniero.");
        return;
    }

    PartidoPolitico nuevo = new PartidoPolitico(nombre, siglas, "sin_logo.png", lider);
    boolean exito = motor.getGestorPartidos().registrarPartido(nuevo);

    if (exito) {
        JOptionPane.showMessageDialog(this, "Partido registrado con éxito.");
        actualizarTabla(); 
        limpiarCampos();   
    } else {
        JOptionPane.showMessageDialog(this, "Error: El arreglo está lleno o el partido ya existe.");
    }
    }//GEN-LAST:event_btnGuardarActionActionPerformed

