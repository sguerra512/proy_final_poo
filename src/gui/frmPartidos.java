    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String sigla = txtSiglas.getText().trim();
        String nuevoNombre = txtNombre.getText().trim();
        String nuevoRepresentante = txtLider.getText().trim();

        if (sigla.isEmpty() || nuevoNombre.isEmpty() || nuevoRepresentante.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete todos los campos antes de modificar.");
            return;
        }

        PartidoPolitico partido = motor.getGestorPartidos().buscarPorSigla(sigla);

        if (partido != null) {
            partido.setNombre(nuevoNombre);
            partido.setRepresentanteLegal(nuevoRepresentante);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Partido modificado con éxito.");
            
            txtSiglas.setText("");
            txtNombre.setText("");
            txtLider.setText("");
            
            actualizarTabla(); 
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se pueden modificar las siglas.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

