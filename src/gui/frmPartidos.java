    private void actualizarTabla() {
    DefaultTableModel modelo = (DefaultTableModel) tblPartidos.getModel();
    
    modelo.setRowCount(0);
    
    PartidoPolitico[] lista = motor.getGestorPartidos().getBdPartidos();
    int total = motor.getGestorPartidos().getCantidadActual();
    
    for (int i = 0; i < total; i++) {
        Object[] fila = {
            lista[i].getNombre(),
            lista[i].getSigla(),
            lista[i].getRepresentanteLegal()
        };
        modelo.addRow(fila);
    }
    
}
    private void limpiarCampos() {
    txtNombre.setText("");
    txtSiglas.setText("");
    txtLider.setText("");
    txtNombre.requestFocus(); 
}
    
    

