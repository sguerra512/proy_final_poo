    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String dni = txtDni.getText().trim();
        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();

        if (dni.isEmpty() || txtDni.isEditable()) {
            JOptionPane.showMessageDialog(this, "Primero seleccione un miembro dando clic en la tabla.");
            return;
        }

        if (nombre.isEmpty() || apellido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Los nombres y apellidos no pueden estar vacíos.");
            return;
        }

        MesaElectoral mesa = motor.getGestorMesas().buscarPorId(idMesaActual);
        
        if (mesa != null) {
            MiembroDeMesa[] miembros = mesa.getMiembros();
            MiembroDeMesa miembroEncontrado = null;

            for (int i = 0; i < mesa.getContadorMiembros(); i++) {
                if (miembros[i].getDni().equals(dni)) {
                    miembroEncontrado = miembros[i];
                    break;
                }
            }

            if (miembroEncontrado != null) {
                miembroEncontrado.setNombres(nombre);
                miembroEncontrado.setApellidos(apellido);
                
                String cargoSeleccionado = cmbCargo.getSelectedItem().toString().toUpperCase();
                miembroEncontrado.setCargo(TipoCargo.valueOf(cargoSeleccionado));

                JOptionPane.showMessageDialog(this, "Miembro modificado con éxito.");

                txtNombre.setText("");
                txtApellido.setText("");
                txtDni.setText("");
                txtDni.setEditable(true);

                actualizarTablaMiembros();
            } else {
                JOptionPane.showMessageDialog(this, "Error crítico: El miembro no existe en esta mesa.");
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void tblMiembrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMiembrosMouseClicked
        int fila = tblMiembros.getSelectedRow();
        if (fila == -1) return;

        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblMiembros.getModel();

        txtNombre.setText(modelo.getValueAt(fila, 0).toString());
        txtApellido.setText(modelo.getValueAt(fila, 1).toString());
        txtDni.setText(modelo.getValueAt(fila, 2).toString());
        cmbCargo.setSelectedItem(modelo.getValueAt(fila, 3).toString());

        txtDni.setEditable(false);
    }//GEN-LAST:event_tblMiembrosMouseClicked

    private void actualizarTablaMiembros() {
    DefaultTableModel modelo = (DefaultTableModel) tblMiembros.getModel();
    modelo.setRowCount(0);

    MesaElectoral mesa = motor.getGestorMesas().buscarPorId(idMesaActual);
    
    if (mesa == null) return; 

    MiembroDeMesa[] miembros = mesa.getMiembros();
    int totalMiembros = mesa.getContadorMiembros();

    for (int i = 0; i < totalMiembros; i++) {
        Object[] fila = {
            miembros[i].getNombres(),
            miembros[i].getApellidos(),
            miembros[i].getDni(),
            miembros[i].getCargo().name()
        };
        modelo.addRow(fila);
    }
}
    
    private void llenarComboCargos() {
        cmbCargo.removeAllItems(); 
        
        for (TipoCargo cargo : TipoCargo.values()) {
            cmbCargo.addItem(cargo.name()); 
        }
    }
    
