    private void btnAgregarMiembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarMiembroActionPerformed
       String nombre = txtNombre.getText().trim();
    String apellido = txtApellido.getText().trim();
    String dni = txtDni.getText().trim();
    
   
    if (nombre.isEmpty() || apellido.isEmpty() || dni.isEmpty()) {
        JOptionPane.showMessageDialog(this, "La red está en medio, no la cruces. Llena todos los campos.");
        return;
    }

    String cargoSeleccionado = cmbCargo.getSelectedItem().toString().toUpperCase();
    TipoCargo cargoElegido = TipoCargo.valueOf(cargoSeleccionado);

    MiembroDeMesa nuevoMiembro = new MiembroDeMesa(nombre, apellido, dni, cargoElegido);

    boolean exito = motor.getGestorMesas().agregarMiembroAMesa(idMesaActual, nuevoMiembro);

    if (exito) {
        JOptionPane.showMessageDialog(this, "Miembro asignado a la mesa " + idMesaActual);
        actualizarTablaMiembros();
    } else {
        JOptionPane.showMessageDialog(this, "Error: La mesa está llena, el DNI se repite, o ese cargo ya está ocupado.");
    }
    }//GEN-LAST:event_btnAgregarMiembroActionPerformed

    private void btnEliminarMiembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMiembroActionPerformed
        int fila = tblMiembros.getSelectedRow();
    
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona a quién vas a sacar de la mesa.");
        return;
    }

    Object objDni = tblMiembros.getValueAt(fila, 2);
    if (objDni == null) return; 

    String dni = objDni.toString();

    int resp = JOptionPane.showConfirmDialog(this, 
        "¿Retirar al miembro con DNI " + dni + " de la mesa " + idMesaActual + "?", 
        "Confirmar", JOptionPane.YES_NO_OPTION);
        
    if (resp == JOptionPane.YES_OPTION) {
        boolean exito = motor.getGestorMesas().eliminarMiembroDeMesa(idMesaActual, dni);
        
        if (exito) {
            actualizarTablaMiembros();

        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar.");
        }
    }
    }//GEN-LAST:event_btnEliminarMiembroActionPerformed

